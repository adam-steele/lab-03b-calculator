export function addition(firstValue, secondValue) {
    
    const sum = firstValue + secondValue;
    return sum;

}

export function subtraction(firstValue, secondValue) {

    
    const subtract = firstValue - secondValue;
    return subtract;

    
    

}

export function multiply(firstValue, secondValue) {

    const multiply = firstValue * secondValue;
    return multiply;
}

    
export function divide(firstValue, secondValue) {
    
    const divide = firstValue / secondValue;
    return divide;
}